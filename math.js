function soma(a, b) {

    //soma a e b
    return a+b
}
function subtracao(a, b) {
    //subtrai a e b
    return a-b
}

function divisao(a, b) {
    //divide  a e b
    return a/b
}

function multiplicacao(a, b) {
    //mutiplica a e b
    return a*b
}

module.exports = {
    soma,
    subtracao,
    divisao,
    multiplicacao
};