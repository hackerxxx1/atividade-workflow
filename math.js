function soma(a, b) {
    //soma
    return a+b
}
function subtracao(a, b) {
    //subtrai
    return a-b
}

function divisao(a, b) {
    //divide
    return a/b
}

function multiplicacao(a, b) {
    //mutiplica
    return a*b
}

module.exports = {
    soma,
    subtracao,
    divisao,
    multiplicacao
};